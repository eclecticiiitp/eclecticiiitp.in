                        <h3>Eclectic Visits BITS Pilani</h3>
                        <div class="explanation">
                            <p>Tired faces, travelling since two days, we reached Bits Pilani, Rajasthan and while we
                                were
                                getting our ID cards we were greeted with almost 20000 people in one single campus. Yes
                                that's how huge Oasis is.
                            </p>
                            <br>
                            <p> This fest never stops. From going in for quizzes at 2AM to getting up at 8 AM for the
                                JAM,
                                Oasis'2018 is an experience of a lifetime. It's more than a college festival.
                                We Participated in all quizzing events and Turncoat and JAM in Literary Events, and
                                Kumar
                                Prakarsh, IIIT Pune made history when he made it to the finals of JAM, when he became
                                the
                                first non- BITSian to qualify for the JAM finals in the last 7 years of Oasis. Kartik
                                Ramaswamy, the renowned JAM master praised this lad when he finished 4th in the Top 8.
                                On Day 2, the Quizzing trio of Rakshit Ranjan, Kumar Prakarsh and Reetik Ranjan (BITS
                                Pilani) finished 3rd in The Music Quiz, Oasis and also made it to the finals of The
                                General
                                Quiz.
                            </p>
                            <br>
                            <p>It was once in a lifetime experience to witness a college fest that never
                                stops(literally),
                                exploring the campus as late as 3 AM in the morning and witnessing one of the best
                                performances by Shankar-Ehsaan-Loy and Abhishek Upmanyu.</p>
                            <br>
                            <p> Oasis'18 will be etched in our memories.</p>
                        </div>
