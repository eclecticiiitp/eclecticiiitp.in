                <h1>This is Eclectic</h1>
                <div class="text-content">
                    <p>
                        From our humble beginnings, just a year after the inception of our parent institute, to its
                        full-fledged form now. “Eclectic”, the literary and debating society of Indian Institute of
                        Information Technology Pune has seen all the heights and depths in the many years of its
                        existence.
                        What started just as a club for literary enthusiasts in the institute to come together and
                        colloquially “hang-out” has now become a professional society with a widespread and a respected
                        community with a social responsibility, credit to which would definitely go to our members who
                        have
                        represented us on various national platforms. 
                    </p>

                    <p>
                        We see, we forget. We speak, we know. We write, we remember. As a community, we aim to instil
                        the
                        same feeling amongst our members. We promote public speaking and writing as forms of
                        self-expression. The way to do them rightly in a way which has a substantial impact needs
                        dedication
                        and a motivation towards the cause and language, and we as a community try to encourage and
                        support
                        the best to our abilities, anyone interested and willing.</p>
                    <div class="container">
                        <ol class="list-unstyled">
                            <li id="one">
                                <h5> Eclectic is founded</h5>
                                <p>In a humble Lecture theatre, a few enthusiasts come together and Eclectic, the club
                                    is
                                    formed.</p>
                            </li>
                            <li id="two">
                                <h5> It’s Literae Fiesta</h5>
                                <p>Eclectic organizes its first ever Literature fest witnessing huge numbers, the
                                    journey is
                                    now set afoot.</p>
                            </li>
                            <li id="three">
                                <h5>Pan India outreach</h5>
                                <p>Celebrating its 2nd anniversary Eclectic's representatives attend even more major
                                    literary events across the nation.
                                </p>
                            </li>
                            <li id="four">
                                <h5>Eclectic goes Online</h5>
                                <p> Even in these troubled times of the pandemic, we stayed connected as a community,
                                    organising online discussions and virtual critique conferences.  
                                </p>
                            </li>
                        </ol>
                    </div>
                    <p>
                        Our vision is to promote the importance of good communication skills both spoken and written in
                        the
                        academic scene as it directly transcends into the professional world. In our country, talented
                        students are falling behind and are not able to achieve what they would have otherwise achieved,
                        because of lack of better communication skills.</p>
                    <p>
                        As a community, we wish to empower students to develop skills they require to gain a competitive
                        edge, we understand our social responsibility and we take every step we can to start-up a
                        discussion. 
                    </p>

