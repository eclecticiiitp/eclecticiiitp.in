                <div class="text-content container" style="width:100%;">

                    <div class="row">
                        <div class="col-md">
                            <p>
                                With our cultural backgrounds hailing from Kolkata to Rajkot and Kashmir to Kanyakumari,
                                Eclectic isn’t just a club but a continuously growing group of individuals with a common
                                thought and shared responsibility to build a literary society. A society that aims to
                                encourage interests towards language and literature and help develop analytical and
                                creative
                                thinking skills.
                            </p>
                            <p>
                                Here at Eclectic, we organize a whole gamut of activities ranging from workshops,
                                competitions, contests to community gatherings where constructive criticism is exercised
                                and
                                appreciated in discourses.
                            </p>
                        </div>
                        <div class="col-md ">
                            <p>
                                Our meet-up discussions include everything from general issues to
                                esthetics and from political agendas to philosophical aspects of life.
                            </p>
                            <p>
                                In a nutshell, Eclectic, the official Literary and Debating Society of IIIT Pune,
                                represents
                                the perspectives and ideologies of not so geeky nerds who exchange thoughts, ethics and
                                beliefs and work together to create a better environment for self growth and inquisitive
                                learning.
                            </p>
